    public void getcashier()
    {
        ResultSet resultset_obj = DBLoader.executeQuery("select * from cashier");
        
         try {
             while(resultset_obj.next())
             {
                cashierList.add(new cashierinfo(resultset_obj.getInt("CID") , resultset_obj.getString("Username"), resultset_obj.getString("Password")));
                
                jTable1.setRowHeight(100);
                
                cashiertablemodel_obj.fireTableDataChanged();
                
             }
         } 
         
         catch (SQLException ex) {
             Logger.getLogger(cashier.class.getName()).log(Level.SEVERE, null, ex);
         }
         
        
    }

