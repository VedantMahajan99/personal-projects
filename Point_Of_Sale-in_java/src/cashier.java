    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
          if((tf_cashier_username.getText() == null)  ||  (tf_cashier_pass.getText() == null))
        {
            JOptionPane.showMessageDialog(rootPane, "ALL FIELDS ARE NECESSARY !");
        }
        else
        {
         
            try
            {
            ResultSet resultset_obj = DBLoader.executeQuery("select * from cashier where Username='"+tf_cashier_username.getText()+"'");
            
            if(resultset_obj.next())
            {
                
                JOptionPane.showMessageDialog(rootPane, "Cashier already exists");
                
            }
            else
            {
                
                resultset_obj.moveToInsertRow();
                
                resultset_obj.updateString("Username", tf_cashier_username.getText());
                
                resultset_obj.updateString("Password", tf_cashier_pass.getText());
                
                resultset_obj.insertRow();
                
                JOptionPane.showMessageDialog(rootPane, "DATA ENTRY SUCCESSFUL !");
                
            }
            
            cashierList.clear();
//            
            getcashier();
            
            
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
         
        int rowselected = jTable1.getSelectedRow();
        
        if(rowselected < 0)
        {
            JOptionPane.showMessageDialog(rootPane, "No Row Selected !");
        }
        else
        {
            String nameselected = cashierList.get(rowselected).cashier_username;
            
            ResultSet resultset_obj = DBLoader.executeQuery("select * from cashier where Username='"+ nameselected +"'");
            
            try 
            {
                resultset_obj.next();
                
                resultset_obj.deleteRow();
            } 
            catch (SQLException ex) 
            {
                Logger.getLogger(cashier.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            cashierList.clear();
            
            getcashier();
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    
      class cashiertablemodel extends AbstractTableModel
    { 
          
        @Override
        public int getRowCount()
        {
            return cashierList.size();
        }
        
        @Override
        public int getColumnCount()
        {
            return 3;
        }
        
        @Override
        public Object getValueAt(int i, int j)
        {
            cashierinfo catgoryinfo_object = cashierList.get(i);
             
             if(j==0)
                 return catgoryinfo_object.CID;
             
             else if(j==1)
                 return catgoryinfo_object.cashier_username;
             
             else if(j==2)
                 return catgoryinfo_object.cashier_password;
             
             return null;
             
        }
        
        @Override
        public String getColumnName(int j)
        {
           String col[]={"CID","Username","Password"}; 
           
           return col[j];
        }
        
        
    }    
    
    
    
    
    
    
    
    
